package com.MacbookStore.ViewModel;

public class ProductViewModel {
    final String Color;
    final String Cpu;
    final String DisplayCard;
    final String Display;
    final String Group;
    final String HardDrive;
    final String Ram;
    final String Year;

    public ProductViewModel(String color, String cpu, String displayCard, String display, String group, String hardDrive, String ram, String year) {
        Color = color;
        Cpu = cpu;
        DisplayCard = displayCard;
        Display = display;
        Group = group;
        HardDrive = hardDrive;
        Ram = ram;
        Year = year;
    }
}
